	<p class="heading">Why we Made This App</p>
	<div class="text">
		<p>Lots of teenagers in Maine want jobs. They want to make money that’s theirs to spend, and if their parents won’t pay for everything, teenagers might also want to save up for the future. While teenagers can’t make the same kind of time commitments that adults do, they can still do all sorts of things, including mow lawns, shovel snow, wash dishes and work entry level retail, just to name a few examples.</p> 

		<p>The problem is that it can be hard to understand how to go about finding and starting a job because teenagers are too young for many of the jobs that get officially listed online, and the application and permitting process can also be very confusing. Maine’s teenagers need help finding opportunities and navigating that process. </p>
	</div>

	<p class="heading"> How to Use the App </p>

	<div class="text">
		<p> Along the footer, you will see four buttons HOME, EMPLOYERS and INFO.</p>
    
		<p>The HOME screen is all of the available jobs. It is designed to be a list of jobs seeking people from 14-20 years old. At the top you will find the job that has been marked as "sponsored" in our database.</p> 

		<p>The EMPLOYERS page is designed to be a list of local employers seeking to hire young people. When the user clicks on the name of an employer, the see the list of currently being offered by that employer.</p>

		<p>The ABOUT page is where you are currently. This page outlines the app's use and tells you a little about the developers. It also provides links to important information regarding the project and the idea the sparked it.</p>

	</div>
 
